#include <stdio.h>
/**
 * add - add function
 * @a: int
 * @b: int
 * Return: int
 */
int add(int a, int b) {
    return a + b;
}

/**
 * sub - sub
 * @a: int
 * @b: int
 * Return: int
 */
int sub(int a, int b) {
    return a - b;
}

/**
 * mul - mul
 * @a: a
 * @b: int
 * Return: int
 */
int mul(int a, int b) {
    return a * b;
}

/**
 * div - div
 * @a: int
 * @b: int
 * Return: int
 */
int div(int a, int b) {
    return a / b;
}

/**
 * mod - mod
 * @a: int
 * @b: int
 * Return: int
 */
int mod(int a, int b) {
    return a % b;
}
